import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    # input value
    s1 = []
    s2 = []
    cur1 = l1
    cur2 = l2
    while cur1 is not None:
        s1.append(cur1.val)
        cur1 = cur1.next
    while cur2 is not None:
        s2.append(cur2.val)
        cur2 = cur2.next
    # make reverse
    x = ''
    y = ''
    for i in range(len(s1)):
        x += str(s1[-1-i])
    for i in range(len(s2)):
        y += str(s2[-1-i])
    sum_of_lt = int(x) + int(y)
    sum_of_lt = str(sum_of_lt)
    # make ListNode
    link_list = None
    for i in range(len(sum_of_lt)):
        val = sum_of_lt[i]
        new_node = ListNode(int(val), None)
        # first node
        if link_list is None:
            link_list = new_node
        else:
            node_new = ListNode(int(val), link_list)
            link_list = node_new
    return link_list


